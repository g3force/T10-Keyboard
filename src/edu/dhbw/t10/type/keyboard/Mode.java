package edu.dhbw.t10.type.keyboard;

/**
 * this class shall contain shift, strg, etc...
 * 
 * @author dirk
 * 
 */
public class Mode {
	// --------------------------------------------------------------------------
	// --- variables and constants ----------------------------------------------
	// --------------------------------------------------------------------------
	private String	mode;
	
	// --------------------------------------------------------------------------
	// --- constructors ---------------------------------------------------------
	// --------------------------------------------------------------------------
	public Mode(String pMode) {
		mode = pMode;
	}


	// --------------------------------------------------------------------------
	// --- methods --------------------------------------------------------------
	// --------------------------------------------------------------------------

	
	// --------------------------------------------------------------------------
	// --- getter/setter --------------------------------------------------------
	// --------------------------------------------------------------------------
	public String getMode() {
		return mode;
	}
	
	
	public void setMode(String mode) {
		this.mode = mode;
	}


}
